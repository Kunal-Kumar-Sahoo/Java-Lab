class Hello {
    // public -> accessible outside the class.
    // static -> the method belongs to the class itself and not to objects of the class.
    // main -> System defined name
    public static void main(String[] args) {
        // There are two types of datatypes in Java: Primitive and Wrapper class.
        // In Java with each primitive datatype there is an associated Wrapper class
            // int -> Integer, float -> Float, double -> Double
            // String -> Wrapper class;
        System.out.println("Hello, World!");
    }
}
